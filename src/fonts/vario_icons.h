/**
 * @brief Obtient la police d'icônes selon la taille
 * @param size Taille souhaitée (12, 14, 16, 20, 24, 28, 32, 48)
 * @return Pointeur vers la police appropriée
 */
static inline const lv_font_t* vario_icons_get_font(uint8_t size) {
    switch (size) {
        case 12: return &vario_icons_12;
        case 14: return &vario_icons_14;
        case 16: return &vario_icons_16;
        case 20: return &vario_icons_20;
        case 24: return &vario_icons_24;
        case 28: return &vario_icons_28;
        case 32: return &vario_icons_32;
        case 48: return &vario_icons_48;
        default: return &vario_icons_24;  // Défaut
    }
}

